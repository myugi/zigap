package vo;

public class BudgetBean {

	private String YYYYMM;
	private int CATEGORY_NO;
	private String INCM_EXP;
	private String BUDGET_ID;
	private int AMT;
	private String CATEGORY_CONTENT;
	
	public String getYYYYMM() {
		return YYYYMM;
	}
	public void setYYYYMM(String yYYYMM) {
		YYYYMM = yYYYMM;
	}
	public int getCATEGORY_NO() {
		return CATEGORY_NO;
	}
	public void setCATEGORY_NO(int cATEGORY_NO) {
		CATEGORY_NO = cATEGORY_NO;
	}
	public String getINCM_EXP() {
		return INCM_EXP;
	}
	public void setINCM_EXP(String iNCM_EXP) {
		INCM_EXP = iNCM_EXP;
	}
	public String getBUDGET_ID() {
		return BUDGET_ID;
	}
	public void setBUDGET_ID(String bUDGET_ID) {
		BUDGET_ID = bUDGET_ID;
	}
	public int getAMT() {
		return AMT;
	}
	public void setAMT(int aMT) {
		AMT = aMT;
	}
	public String getCATEGORY_CONTENT() {
		return CATEGORY_CONTENT;
	}
	public void setCATEGORY_CONTENT(String cATEGORY_CONTENT) {
		CATEGORY_CONTENT = cATEGORY_CONTENT;
	}
	
	
}
